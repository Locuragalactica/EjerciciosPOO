

package entidades;


public class Cadena {
private String frase;
private int longe;

    public Cadena() {
    }

    public Cadena(String frase, int longe) {
        this.frase = frase;
        this.longe = longe;
    }

    public String getFrase() {
        return frase;
    }

    public void setFrase(String frase) {
        this.frase = frase;
    }

    public int getLonge() {
        return longe;
    }

    public void setLonge(int longe) {
        this.longe = longe;
    }
    

    
    
}
